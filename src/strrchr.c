#include <posix/stddef.h>

/**
 * The __strrchr() function locates the last occurrence of @p c
 * (converted to a char) in the string pointed to by @p s. The
 * terminating null character is considered to be part of the string.
 */
char *__strrchr(const char *s, int c)
{
	const char *last;

	last = NULL;

	while (*s != '\0')
	{
		if (*s == c)
			last = s;
		s++;
	}

	if (*s == c)
		last = s;

	return ((char *) last);
}
